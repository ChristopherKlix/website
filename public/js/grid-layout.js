function featureScroll() {
    console.log('featureScroll() called...');
    
    for (let i = 1; i <= 4; i++) {
        $(`#card-feature-${i}`).click(function() {            
            const target = $(`#card-${i + 1}`);
            $('html, body').animate({
                scrollTop: target.offset().top - 120
            }, 700, 'easeInOutCubic');
        });
    }
};

