function initGrid() {
    console.log('initGrid() called...');

    let divPlaceholder = `<div id="card-menu"></div>`;
    $('body').append(divPlaceholder);
    $(`#card-menu`).load(`episodes/epMenu.html`, function(responseTxt, statusTxt, xhr){
        $('button:nth-child(1)').on('click', function() {
            window.location.href = './index.html';
        });
    });

    divPlaceholder = `<div id="card-feature"></div>`;
    $('#cards-grid').append(divPlaceholder);
    $(`#card-feature`).load(`episodes/epFeature.html`, function(responseTxt, statusTxt, xhr){
        $.getJSON("./episodes/featuredEpisodes.json", function(episodes) {
            for (let i = 1; i < episodes.episodes.length; i++) {
                const heroImg = `./episodes/ep${i}/hero-ep_${i}.jpg`;
                $(`#card-feature-${i} .feature-hero`).css('background-image', `url(${heroImg})`);
                const epNumber = `ep. ${episodes.episodes[i].epNumber}`
                $(`#card-feature-${i} .feature-ep`).html(epNumber);
                $(`#card-feature-${i} .feature-title`).html(episodes.episodes[i].epTitle);
                $(`#card-feature-${i} .feature-subtitle`).html(episodes.episodes[i].epSubtitle);
                $(`#card-feature-${i} .feature-date`).html(episodes.episodes[i].epDate);
                let hashString = '';
                for (let hash = 0; hash < episodes.episodes[i].epHash.length; hash++) {
                    hashString = hashString + ' ' + episodes.episodes[i].epHash[hash];
                };
                $(`#card-feature-${i} .feature-hash`).html(hashString);
            }
            featureScroll();
        });


    });
    
    for (let i = 1; i < 3; i++) {
        const divPlaceholder = `<div id="card-${i + 1}"></div>`;
        $('#cards-grid').append(divPlaceholder);
        $(`#card-${i + 1}`).load(`episodes/ep${i}/ep${i}.html`, function(responseTxt, statusTxt, xhr){
        });
    }

    divPlaceholder = `<div id="card-archive"></div>`;
    $('#cards-grid').append(divPlaceholder);
    $(`#card-archive`).load(`episodes/epArchive.html`, function(responseTxt, statusTxt, xhr){
    });
}

